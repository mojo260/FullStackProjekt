package com.example.wishlistproject.wishlist;

public class Wish {
    private int wishId;
    private String wishTekst;
    public Wish() {
    }

    public int getWishId() {
        return wishId;
    }

    public void setWishId(int wishId) {
        this.wishId = wishId;
    }

    public String getWishTekst() {
        return wishTekst;
    }

    public void setWishTekst(String wishTekst) {
        this.wishTekst = wishTekst;
    }
}
